
package mvc.model.prestadordeservico;

public class CadastroPrestadorDeServicos {
    private int id;
    private String login;
    private String nomePrestadorDeServicos;
    private String Email;
    private String Senha;
    private String telefone;
    private String endereco;
    private String cpf;
    private String trabalhosRealizados;
    private String valorDiaria;
    private String agendaDisponibilidade;
    private String anosExperiencia;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getNomePrestadorDeServicos() {
        return nomePrestadorDeServicos;
    }

    public void setNomePrestadorDeServicos(String nomePrestadorDeServicos) {
        this.nomePrestadorDeServicos = nomePrestadorDeServicos;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getSenha() {
        return Senha;
    }

    public void setSenha(String Senha) {
        this.Senha = Senha;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getTrabalhosRealizados() {
        return trabalhosRealizados;
    }

    public void setTrabalhosRealizados(String trabalhosRealizados) {
        this.trabalhosRealizados = trabalhosRealizados;
    }

    public String getValorDiaria() {
        return valorDiaria;
    }

    public void setValorDiaria(String valorDiaria) {
        this.valorDiaria = valorDiaria;
    }

    public String getAgendaDisponibilidade() {
        return agendaDisponibilidade;
    }

    public void setAgendaDisponibilidade(String agendaDisponibilidade) {
        this.agendaDisponibilidade = agendaDisponibilidade;
    }

    public String getAnosExperiencia() {
        return anosExperiencia;
    }

    public void setAnosExperiencia(String anosExperiencia) {
        this.anosExperiencia = anosExperiencia;
    }
    
    
    
    
}
