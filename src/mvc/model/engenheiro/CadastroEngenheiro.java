
package mvc.model.engenheiro;


public class CadastroEngenheiro {
    
      
    //atributos da classe animal
    private int id;
    private String login;
    private String nome;
    private String Email;
    private String Senha;
    private String telefone;
    private String endereco;
    private String cpf;
    private String numeroCau;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getSenha() {
        return Senha;
    }

    public void setSenha(String Senha) {
        this.Senha = Senha;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNumeroCau() {
        return numeroCau;
    }

    public void setNumeroCau(String numeroCau) {
        this.numeroCau = numeroCau;
    }
    
    
    
}
